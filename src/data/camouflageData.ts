export function generateRandomCamouflageRows(count: number): CamouflageRow[] {
  const rows: CamouflageRow[] = [];
  for (let i = 0; i < count; i++) {
    const region = regions[randomNum(0, regions.length - 1)];
    const product = products[randomNum(0, products.length - 1)];
    const q1 = randomNum(100, 2000) * 100;
    const q2 = Math.round(q1 * (1 + randomNum(5, 20) / 100));
    const q3 = Math.round(q2 * (1 + randomNum(5, 20) / 100));
    const q4 = Math.round(q3 * (1 + randomNum(5, 20) / 100));
    const total = q1 + q2 + q3 + q4;
    const growth = (randomNum(8, 25)) + "." + randomNum(1, 9) + "%";
    rows.push({
      A: region,
      B: product,
      C: "¥" + q1.toLocaleString(),
      D: "¥" + q2.toLocaleString(),
      E: "¥" + q3.toLocaleString(),
      F: "¥" + q4.toLocaleString(),
      G: "¥" + total.toLocaleString(),
      H: growth,
    });
  }
  return rows;
}

